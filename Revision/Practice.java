public  class Practice
{
    public static  void main(String args[])
    {
        int a=5;
         System.out.println("a++ : "+(a++));// print a=5 value 6;
          System.out.println("a : "+(a)); //value 6;
         System.out.println("++a : "+(++a));// print 7 value 7,
         System.out.println("a : "+(a)); //value 7
         System.out.println("a-- : "+(a--));// Print 7 value 6
         System.out.println("a : "+(a)); // value 6
         System.out.println("--a : "+(a--));// 6 value 5
          System.out.println("a : "+(a)); // value 6

    }
}